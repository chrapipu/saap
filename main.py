bot = telebot.TeleBot(config.TOKEN)
user_data = {}

def main_menu_keyboard():
    markup = types.ReplyKeyboardMarkup(resize_keyboard=True, one_time_keyboard=True)
    btn1 = types.KeyboardButton("üí™ –¢—Ä–µ–Ω–∏—Ä–æ–≤–æ—á–Ω—ã–π –ø–ª–∞–Ω")
    btn2 = types.KeyboardButton("ü•ó –°–æ–≤–µ—Ç—ã –ø–æ –ø–∏—Ç–∞–Ω–∏—é")
    markup.add(btn1, btn2)
    return markup

def weight_selection_keyboard():
    markup = types.ReplyKeyboardMarkup(resize_keyboard=True)
    weights = [f"{i}-{i+9} –∫–≥" for i in range(50, 100, 10)]
    for weight in weights:
        markup.add(types.KeyboardButton(weight))
    return markup

