# Ввод строки с клавиатуры
input_string = input("Введите строку: ")

# Разбиваем строку на слова, предполагая, что слова разделены пробелами
words = input_string.split()

# Инициализируем переменные для подсчета слов с четной длиной и для хранения самого длинного слова
even_length_count = 0
longest_word = ""

# Проходим по каждому слову в списке
for word in words:
    # Подсчитываем слова с четной длиной
    if len(word) % 2 == 0:
        even_length_count += 1

    # Сравниваем длину текущего слова с длиной самого длинного слова, которое мы видели до этого
    if len(word) > len(longest_word):
        longest_word = word

# Выводим результаты
print("Количество слов с четной длиной:", even_length_count)
print("Самое длинное слово:", longest_word)
